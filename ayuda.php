<h2>Procedimiento para evaluar un objeto en ACCIO</h2>
<ol>
	<li>Por el momento ACCIO sólo valida documentos en formato DOCX y ODT</li>
	<li>Localice el objeto a validar en su computadora.</li>
	<li>Haga clic en el botón “Subir archivo”. Seleccione el archivo y haga clic en “Abrir”</li>
	<li>Pulse el botón “Evaluar”</li>
	<li>En unos pocos segundos la herramienta le devolverá:<br>
Un porcentaje de accesibilidad<br>
Los parámetros y recomendaciones que son válidos y accesibles en su objeto<br>
Los parámetros y recomendaciones que debe mejorar y cómo hacerlo.</li>
	<li>La herramienta ofrece la posibilidad de descargar un reporte con las mismas recomendaciones indicadas en el lado derecho de la pantalla. Dichas recomendaciones han sido creadas en base estándares y apuntando a la creación de documentos de formato simplificado.</li>
</ol> <br>
<h2>¿Como interpretar el dato de porcentaje ofrecido por ACCIO?</h2>
<ul>
	<li>Hasta un <b>40%</b> su documento no es accesible.</li>
	<li>Entre <b>50% y 69%</b> su documento cumple con algunos parámetros de accesibilidad.</li>
	<li>A partir de un <b>70%</b> en adelante cumple la mayoría de los parámetros de accesibilidad.</li>
</ul>
